module top#(
    BODY_INIT_PERIOD = 'd40000000,
    RAN_GEN_CLK_PERIOD = 'd10000
)(
    input rst,
    input set_seed,
    input system_clk,
    input up_but,
    input right_but,
    input down_but, //higher freq
    input left_but,
    output [15:0] display//indeed used
);
    wire clk_12500Hz;
    wire clk_forBody;

    snake_top snake_top(
      .rst(rst),
      .clk(clk_forBody),
      .set_seed(set_seed),
      .clk_12500Hz(clk_12500Hz),
      .up_but(up_but),
      .right_but(right_but),
      .down_but(down_but),
      .left_but(left_but),
      .display(display)
    );

    freq_divider #(
    .BODY_INIT_PERIOD(BODY_INIT_PERIOD),
    .RAN_GEN_CLK_PERIOD(RAN_GEN_CLK_PERIOD)
    )freq_divider(
      .rst(set_seed),
      .clk_125MHz(system_clk),
      .clk_12500Hz(clk_12500Hz),
      .clk_forBody(clk_forBody)
    );
    
 endmodule
